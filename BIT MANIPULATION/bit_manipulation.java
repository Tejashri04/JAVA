public class bit_manipulation{
    public static void main(String args[]){

        // Bit-wise AND
        System.out.println(5 & 6); 

        // Bit-wise OR
        System.out.println(5 | 6);

        // Bit-wise XOR
        System.out.println(5 ^ 6);

        // Binary One's Complement(NOT operator)
        System.out.println(~5); 

        // Binary Left Shift 
        System.out.println(5 << 2);

         // Binary Right Shift 
        System.out.println(6 >> 1);


    }
}